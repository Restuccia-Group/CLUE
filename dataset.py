    if no_aug:
        train_transform = transforms.Compose([
            transforms.Resize((224, 224)),
            transforms.ToTensor(),
        ])
    else:
        train_transform = transforms.Compose([
            transforms.Resize((256, 256)),
            transforms.RandomCrop(224),
            transforms.RandomHorizontalFlip(),
            transforms.ToTensor(),
        ])

    test_transform = transforms.Compose([
        transforms.Resize((224, 224)),
        transforms.ToTensor(),
    ])

    print(
        f"Dataset information: Caltech-256\t split={train_ratio:.0%} train / "
        f"{val_ratio:.0%} val / {(1-train_ratio-val_ratio):.0%} test"
    )
    print("Data augmentation = resize(256) + randomcrop(224) + randomhorizontalflip")

    # root should point to the folder with 256_ObjectCategories
    root = data_dir #os.path.join(data_dir, "256_ObjectCategories")
    full_dataset = ImageFolder(root)

    targets = np.array([y for _, y in full_dataset])
    idx = np.arange(len(targets))

    # 1. split train vs temp
    train_idx, temp_idx, y_train, y_temp = train_test_split(
        idx,
        targets,
        stratify=targets,
        test_size=(1 - train_ratio),
        random_state=seed,
    )

    # 2. split temp into val vs test
    relative_val_ratio = val_ratio / (1 - train_ratio)
    val_idx, test_idx, _, _ = train_test_split(
        temp_idx,
        y_temp,
        stratify=y_temp,
        test_size=(1 - relative_val_ratio),
        random_state=seed,
    )

    # datasets with transforms
    base_train = ImageFolder(root, transform=train_transform)
    base_val   = ImageFolder(root, transform=test_transform)
    base_test  = ImageFolder(root, transform=test_transform)

    train_set = Subset(base_train, train_idx)
    val_set   = Subset(base_val, val_idx)
    test_set  = Subset(base_test, test_idx)

    # attach .data and .targets for CIFAR-like compatibility
    train_set.data = np.array([base_train.samples[i][0] for i in train_idx])
    train_set.targets = np.array([base_train.samples[i][1] for i in train_idx])

    val_set.data = np.array([base_val.samples[i][0] for i in val_idx])
    val_set.targets = np.array([base_val.samples[i][1] for i in val_idx])

    test_set.data = np.array([base_test.samples[i][0] for i in test_idx])
    test_set.targets = np.array([base_test.samples[i][1] for i in test_idx])

    # replacement hooks
    if class_to_replace is not None and indexes_to_replace is not None:
        raise ValueError("Only one of `class_to_replace` and `indexes_to_replace` can be specified")
    if class_to_replace is not None:
        replace_class(
            train_set,
            class_to_replace,
            num_indexes_to_replace=num_indexes_to_replace,
            seed=seed - 1,
            only_mark=only_mark,
        )
        if num_indexes_to_replace is None:
            mask = test_set.targets != class_to_replace
            test_idx = test_idx[mask]
            test_set = Subset(base_test, test_idx)
            test_set.data = np.array([base_test.samples[i][0] for i in test_idx])
            test_set.targets = np.array([base_test.samples[i][1] for i in test_idx])
    if indexes_to_replace is not None:
        replace_indexes(
            dataset=train_set,
            indexes=indexes_to_replace,
            seed=seed - 1,
            only_mark=only_mark,
        )

    # dataloaders
    loader_args = {"num_workers": num_workers, "pin_memory": True}

    def _init_fn(worker_id):
        np.random.seed(int(seed))

    train_loader = DataLoader(
        train_set, batch_size=batch_size, shuffle=shuffle,
        worker_init_fn=_init_fn if seed is not None else None, **loader_args
    )
    val_loader = DataLoader(
        val_set, batch_size=batch_size, shuffle=False,
        worker_init_fn=_init_fn if seed is not None else None, **loader_args
    )
    test_loader = DataLoader(
        test_set, batch_size=batch_size, shuffle=False,
        worker_init_fn=_init_fn if seed is not None else None, **loader_args
    )

    return train_loader, val_loader, test_loader

