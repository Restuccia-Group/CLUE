# Custom Dataset for VGGFace2
class VGGFace2Dataset(Dataset):
    def __init__(self, root_dir, transform=None):
        """
        Args:
            root_dir (string): Directory with all the images.
            transform (callable, optional): Optional transform to be applied
                on a sample.
        """
        self.root_dir = root_dir
        self.transform = transform
        self.classes = os.listdir(root_dir)  # Each folder corresponds to a class (person)
        self.data = []
        self.targets = []

        # Go through each folder and get all image paths and their corresponding labels
        for label, person in enumerate(self.classes):
            person_dir = os.path.join(root_dir, person)
            if os.path.isdir(person_dir):
                for img_name in os.listdir(person_dir):
                    img_path = os.path.join(person_dir, img_name)
                    self.data.append(img_path)
                    self.targets.append(label)

    def __len__(self):
        return len(self.data)

    def __getitem__(self, idx):
        if torch.is_tensor(idx):
            idx = idx.tolist()

        img_path = self.data[idx]
        image = Image.open(img_path).convert("RGB")  # Convert to RGB
        label = self.targets[idx]

        if self.transform:
            image = self.transform(image)

        return image, label

