def caltech256_dataloaders_no_val(
    batch_size=128,
    data_dir="datasets/caltech256",
    num_workers=2,
    seed: int = 1,
    train_ratio: float = 0.7,
    val_ratio: float = 0.1,
):
    """
    Build train/val/test dataloaders for Caltech-256 with explicit stratified splits.
    Adds .targets attribute to each subset for CIFAR-style compatibility.
    """

    # transforms
    train_transform = transforms.Compose([
        transforms.Resize((256, 256)),
        transforms.RandomCrop(224),
        transforms.RandomHorizontalFlip(),
        transforms.ToTensor(),
    ])

    test_transform = transforms.Compose([
        transforms.Resize((224, 224)),
        transforms.ToTensor(),
    ])

    # load the full dataset once
    root = data_dir #os.path.join(data_dir, "256_ObjectCategories")  # adjust if needed
    full_dataset = ImageFolder(root, transform=None)

    # collect labels
    targets = np.array([y for _, y in full_dataset])
    idx = np.arange(len(targets))

    # stratified split: train vs temp
    train_idx, temp_idx, y_train, y_temp = train_test_split(
        idx,
        targets,
        stratify=targets,
        test_size=(1 - train_ratio),
        random_state=seed,
    )

    # stratified split: temp -> val/test
    relative_val_ratio = val_ratio / (1 - train_ratio)
    val_idx, test_idx, _, _ = train_test_split(
        temp_idx,
        y_temp,
        stratify=y_temp,
        test_size=(1 - relative_val_ratio),
        random_state=seed,
    )

    print(
        f"Dataset information: Caltech-256\t"
        f"{len(train_idx)} train \t {len(val_idx)} val \t {len(test_idx)} test"
    )

    # base datasets with transforms
    base_train = ImageFolder(root, transform=train_transform)
    base_val   = ImageFolder(root, transform=test_transform)
    base_test  = ImageFolder(root, transform=test_transform)

    # build subsets
    train_set = Subset(base_train, train_idx)
    val_set   = Subset(base_val, val_idx)
    test_set  = Subset(base_test, test_idx)

    # attach .targets for CIFAR-style compatibility
    train_set.targets = targets[train_idx]
    val_set.targets   = targets[val_idx]
    test_set.targets  = targets[test_idx]

    # dataloaders
    loader_args = {"num_workers": num_workers, "pin_memory": True}

    train_loader = DataLoader(
        train_set,
        batch_size=batch_size,
        shuffle=True,
        **loader_args,
    )
    val_loader = DataLoader(
        val_set,
        batch_size=batch_size,
        shuffle=False,
        **loader_args,
    )
    test_loader = DataLoader(
        test_set,
        batch_size=batch_size,
        shuffle=False,
        **loader_args,
    )

    return train_loader, val_loader, test_loader

